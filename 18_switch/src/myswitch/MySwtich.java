package myswitch;

public class MySwtich {

	public static void main(String[] args) {
		int i = 2;
		//int j = 4;

		switch (i) {
		default:
			System.out.println("default");
			break;
		case 1:
		case 2:
			System.out.println("eins-zwei");
			break;
		case 3:
			System.out.println("drei");
			break;		
		}
		
		//int - Integer
		//byte - Byte
		//short - Short
		//char - Character
		//String
		//enum
	}

}
